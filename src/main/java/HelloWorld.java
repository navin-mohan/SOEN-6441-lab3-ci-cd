/**
 * The type Hello world.
 */
public class HelloWorld {
    /**
     * Add int.
     *
     * @param a the a
     * @param b the b
     * @return the int
     */
    int add(int a, int b) {
        return a + b;
    }

    /**
     * Say hello string.
     *
     * @param name the name
     * @return the string
     */
    String sayHello(String name) {
        return "Hello, " + name + "!";
    }
}
